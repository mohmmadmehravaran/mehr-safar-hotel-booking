// نرمال‌سازی متن فارسی برای جستجو (یکسان‌سازی ی/ك، حذف فاصله و نیم‌فاصله اضافی)
export function normalizeFa(s: string): string {
  return (s || '')
    .replace(/[\u064A\u0649]/g, '\u06CC') // ي/ى → ی
    .replace(/\u0643/g, '\u06A9')        // ك → ک
    .replace(/[\u200c\s]+/g, ' ')         // نیم‌فاصله/فاصله‌ها → یک فاصله
    .trim()
    .toLowerCase();
}

// جستجوی شهرها بر اساس متن ورودی
export function searchCities(query: string, limit = 50): string[] {
  const q = normalizeFa(query);
  if (!q) return [];
  const starts: string[] = [];
  const contains: string[] = [];
  for (const c of IRAN_CITIES) {
    const n = normalizeFa(c);
    if (n.startsWith(q)) starts.push(c);
    else if (n.includes(q)) contains.push(c);
  }
  return [...starts, ...contains].slice(0, limit);
}
